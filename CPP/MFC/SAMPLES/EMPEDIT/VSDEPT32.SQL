 COMM                NUMBER(7,2),
 DEPTNO              NUMBER(2) NOT NULL,
 VACATION            VARCHAR2(20),
 HOBBY		   VARCHAR2(20),
 CONSTRAINT EMP2_FOREIGN_KEY FOREIGN KEY (DEPTNO) REFERENCES DEPT (DEPTNO),
 CONSTRAINT EMP2_PRIMARY_KEY PRIMARY KEY (EMPNO));


